public class Person {
    private String name;
    private String surname;
    private int age;
    private String millati;


    public void setName(String john) {
        name = john;
    }

    public void setSurname(String doe) {
        surname = doe;
    }

    public void setAge(int i) {
        age = i;
    }


    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }

    public void setMillati(String usa) {
          millati = usa;
    }

    public String  getMillati() {
        return millati;
    }
}
